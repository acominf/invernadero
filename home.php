	<p style="text-align: justify;">
		Es la automatizacion de un sistema de fertirrigación en base a las necesidades de las plantas, este sistema cuenta con monitorizacion de factores como humedad de la tierra, humedad relativa, temperatura, PH, CE, luminocidad.
		El usuario puede monitorizar el sistema a travez de la pagina web donde puede conocer en tiempo real los valores de el cultivo en cuanto las variables climaticas. movimientos, riego y por quien se han realizado movimientos.
