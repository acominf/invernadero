		Si promovemos el uso de la automatización de los invernaderos el riesgo de intoxicaciones de los trabajadores por pesticidas seria considerablemente resuelto. También este proyecto busca beneficiar directamente tanto al sistema de cultivo tradicional (campo abierto) como al sistema de ambiente controlado.
